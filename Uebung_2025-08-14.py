print('Berechnen wir die Fläche eines Dreiecks.')
print('Bitte geben Sie nur ganze Zahlen ein.')
breite = int(input('Geben Sie die Basis des Dreiecks ein:'))
hoehe = int(input('Geben Sie die Höhe des Dreiecks ein:'))
